    {
        $okrs = Okr::all();
        foreach ($okrs as $key => $value) {
            $okrs[$key]['text'] = $value->result;
        }

        $sorted = $okrs->sortBy('start_date')->flatten();
        $sorted->values()->all();

        return response()->json([
            "data" => $sorted
        ]);
    }

